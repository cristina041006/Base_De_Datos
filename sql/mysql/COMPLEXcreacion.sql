CREATE TABLE DEPART
(
	DEPT_NO INTEGER(10),
	DNOMBRE VARCHAR(20),
	LOC VARCHAR(20),
	CONSTRAINT PK_DEPT PRIMARY KEY (DEPT_NO)
);

CREATE TABLE EMPLE 
(
	EMP_NO INTEGER(10),
	APELLIDO VARCHAR(20),
	OFICIO VARCHAR(20),
	DIR INTEGER(10),
	FECHA_ALT DATE,
	SALARIO INTEGER(10),
	COMISION INTEGER(10),
	DEPT_NO INTEGER(10),
	CONSTRAINT PK_EMPLE PRIMARY KEY (EMP_NO),
	CONSTRAINT FK_EMPLE FOREIGN KEY (DEPT_NO) REFERENCES DEPART(DEPT_NO)
);

CREATE TABLE HERRAMIENTAS
(
	DESCRIPCION VARCHAR(20),
	ESTANTERIA INT(10),
	UNIDADES INT(10),
	CONSTRAINT PK_HE PRIMARY KEY (DESCRIPCION)
);

CREATE TABLE HOSPITALES 
(
	COD_HOSPITAL INT(10),
	NOMBRE VARCHAR(20),
	DIRECCION VARCHAR(50),
	NUM_PLAZAS INT (10),
	CONSTRAINT PK_HO PRIMARY KEY (COD_HOSPITAL)
);

CREATE TABLE PERSONAS
(
	DNI VARCHAR(9),
	COD_HOSPITAL INT(10),
	APELLIDOS VARCHAR(30),
	FUNCION VARCHAR(20),
	SALARIO INT(10),
	CONSTRAINT PK_PER PRIMARY KEY (DNI),
	CONSTRAINT FK_PER FOREIGN KEY (COD_HOSPITAL) REFERENCES HOSPITALES(COD_HOSPITAL)
);

CREATE TABLE MEDICOS
(
	
	DNI INT(9),
	COD_HOSPITAL INT(1),
	APELLIDOS VARCHAR(50),
	ESPECIALIDAD VARCHAR(30),
	CONSTRAINT PK_MEDICOS PRIMARY KEY (DNI),
	CONSTRAINT FK1_MEDICOS FOREIGN KEY (COD_HOSPITAL) REFERENCES HOSPITALES(COD_HOSPITAL)
);



